// Strings
let name = "Hello";
console.log(name);

let name2 = 'World';
console.log(name2.length);

console.log(name2[0]);
console.log(name2[4]);

// Template literals
let b1 = "Alex";
let b2 = "John";

let sentence = `${b1} is a friend of ${b2}`;
console.log(sentence);
console.log(sentence.length);

// Escape Sequence Character
let fruit = 'Banana\'s';
console.log(fruit);
console.log(fruit.length);