#Operators in Python
"""1.Arithmetic Operator
2.Assignment Operator
3.Comparison Operator
4.Logical Operator
5.Identity Operator
6.Membership Operator
7.Bitwise Operators"""

#1
print("5+6 is ", 5+6)
print("5-6 is ", 5-6)
print("5*6 is ", 5*6)
print("5/6 is ", 5/6)
print("5%6 is ", 5%6) # It will only print the remainder
print("5**6 is ", 5**6) # "**" Means power
print("5//6 is ", 5//6) # "//" It will divide and give the answer without the decimal value

#2
x = 5 # Here "=" is an Assignment Operator
x+=5 # It will add 5 in x

#3
i = 5
print(i==5) # It's used to compare two integer
print(i!=5) # It's also used to compare two integer
print(i<5)
print(i>5)

#4
a = True
b = False
print(a and b)
print(a or b)

#5
print(a is b)
print(a is not b)

#6
