#include<stdio.h>
int main()
{
    int a,b;
    a = 50;
    b = 5;
    printf("Arithmetic Operators\n");
    printf("a = %d\n",a);
    printf("b = %d\n",b);
    printf("a + b = %d \n",a+b);
    printf("a - b = %d \n",a-b);
    printf("a * b = %d \n",a*b);
    printf("a / b = %d \n",a/b);
    return 0;
}